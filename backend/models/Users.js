const mongoose =require("mongoose");
const NoteSchema = new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    date:{
        type:Date,
        default:Date.now
    }
});


const model=mongoose.model("user",NoteSchema);
module.exports = model;